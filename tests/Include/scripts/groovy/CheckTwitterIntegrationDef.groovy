class CheckTwitterIntegrationDef {
	/**
	 * The step definitions below match with Katalon sample Gherkin steps
	 */
	@And("Volto para a activity anterior")
	def I_want_to_return_to_the_previous_activity() {
		Mobile.delay(2)
		Mobile.pressBack()
	}

	@And("Vejo a ImageView {string}")
	def I_want_to_view_the_image(String view_name) {
		Mobile.verifyElementExist(findTestObject('android.widget.ImageView0-'+view_name), 0)
		Mobile.pressBack()
	}


	@And("Nao vejo a ImageView {string}")
	def I_want_know_if_I_can_view_the_image(String view_name) {
		Mobile.verifyElementNotExist(findTestObject('android.widget.ImageView0-'+view_name), 0)
		Mobile.pressBack()
	}
}